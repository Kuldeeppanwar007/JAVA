package Use_of_for_loop.Pattern_loop;

public class PyramidInverted_of_num {
    public static void main(String[] args) {
        for (int i = 5; i >= 1; i--) {//i=1;i<=5;i++
            for (int j = 1; j <= i; j++ ) {//j<=5-i+1;
                System.out.print(" "+j);//j
            }System.out.println();
    }
    }}

    

